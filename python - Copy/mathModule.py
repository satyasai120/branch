import math
#ceil function
print(math.ceil(4.2)) #used to round up to the next integer
print(math.floor(5.9)) #rounds down to the closest integer
print(math.sqrt(25)) #used to return the square root of a number
print(math.pow(2,3)) #returns the power of a number

#constants are used when we want to use exact values of constants in an operation
print(math.pi)
print(math.e)

#log function
print(math.log(math.e))